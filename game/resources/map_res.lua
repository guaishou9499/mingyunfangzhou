-- [条件] 是否自定义场景[指定场景名]
map_res.is_get_area_name = function(area_name)
    local map_name = actor_unit.map_name()
    local map_info = map_res.map_area[map_name]
    if not table.is_empty(map_info) then
        for _,v in pairs(map_info) do
            if v.name == area_name then
                return true
            end
        end
    end
    return false
end

