-------------------------------------------------------------------------------------
-- [读取] 当前地图或指定地图是否为小岛
map_res.is_in_islet = function(map_name)
    map_name = map_name or actor_unit.map_name()
    local city_info = map_res.map_info[map_name]
    if not table.is_empty(city_info) then
        if city_info['所属大陆'] == map_name then
            return true
        end
    end
    return false
end

-------------------------------------------------------------------------------------
-- [读取] 是否在大海中
map_res.is_in_ocean = function()
    return string.find(actor_unit.map_name(),'大航海')
end

-------------------------------------------------------------------------------------
-- [读取] 获取当前大陆主城
map_res.get_main_city = function(map_name)
    map_name        = map_name or actor_unit.map_name()
    local main_city = ''
    local city_info = map_res.map_info[map_name]
    if table.is_empty(city_info) then
        return main_city
    end
    local mainland = city_info['所属大陆']
    if mainland == '卢特兰' then
        main_city = '卢特兰城'
    elseif mainland == '贝隆' then
        main_city = '贝隆城'
    elseif mainland == '阿尔泰因' then
        main_city = '斯特恩'
    elseif mainland == '洛恒戴尔' then
        main_city = '洛亚伦'
    elseif mainland == '永恩' then
        main_city = '伟大之城'
    elseif mainland == '费顿' then
        main_city = '卡拉贾村'
    end
    return main_city
end

-------------------------------------------------------------------------------------
-- [读取] 获取自定义场景名称
map_res.get_map_area_name_ex = function(x,y)
    local map_name = actor_unit.map_name()
    local map_info = map_res.map_area[map_name]
    if not table.is_empty(map_info) then
        x = x or local_player:cx()
        y = y or local_player:cy()
        for _,v in pairs(map_info) do
            local area   = v.area
            if area and utils.is_point_in_polygon(x, y, area) then
                return v.name
            end
        end
    end
    return ''
end

